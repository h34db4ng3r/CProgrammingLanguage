#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{

    int num = 6;

    // %f gives a floating point number back
    printf("%f\n", 8.9);
    printf("%f\n", 5.0 + 4.5);
    printf("%d\n",5 / 4);
    printf("%d\n", num);

    printf("%f\n", pow(2, 3));
    printf("%f\n", sqrt(36));
    printf("%f\n", ceil(36.5633));
    printf("%f\n", floor(36.5633));
    


    return 0;
}